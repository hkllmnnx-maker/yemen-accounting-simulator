  final List<QuizQuestion> quiz;

  const LessonContent({
    required this.id,
    required this.title,
    required this.summary,
    required this.sections,
    required this.yemeniExample,
    required this.practicalExercise,
    this.trainingId,
    required this.quiz,
  });
}

class QuizQuestion {
